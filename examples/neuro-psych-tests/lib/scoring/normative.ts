const INVERTED = new Set(["trail_making_a", "trail_making_b"]);

function findNorm(test: string, age: number, edu: number): NormValue | null {
  const entries = NORMS[test];
  if (!entries) return null;
  for (const [[alo, ahi, elo, ehi], val] of entries) {
    if (age >= alo && age <= ahi && edu >= elo && edu <= ehi) return val;
  }
  return null;
}

export function rawToZScore(test: string, rawScore: number, age: number, educationYears: number): number | null {
  const norm = findNorm(test, age, educationYears);
  if (!norm) return null;
  const [mean, sd] = norm;
  if (sd === 0) return 0;
  let z = (rawScore - mean) / sd;
  if (INVERTED.has(test)) z = -z;
  return Math.round(z * 100) / 100;
}

export function classifyImpairment(zScore: number): string {
  if (zScore >= -1.0) return "Normal";
  if (zScore >= -1.5) return "Low Normal";
  if (zScore >= -2.0) return "Borderline";
  if (zScore >= -2.5) return "Mild Impairment";
  if (zScore >= -3.0) return "Moderate Impairment";
  return "Severe Impairment";
}

export function zToPercentile(z: number): number {
  return Math.round(100 / (1 + Math.exp(-1.7 * z)) * 10) / 10;
}
